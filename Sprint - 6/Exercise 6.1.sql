-- Exercise 1 :  Design an ER Diagram for car Rentals.

-- A car rental company maintains a database to manage its daily operations.
-- The system keeps track of customers,the vehicles avaliable for rent,and rental transactions.

-- Customer can rent multiple cars over time,but each rental is linked to only one customer.
-- Each rental records details like the rental period,the car assigned,and the total cost.
-- Payments are associated with rentals,capturing how customers complete their payments.
-- The system needs to track the avalibilty of cars,ensuring no double bookings.

-- Task : Identify key entities and relationships,then design an ER diagram that effectively represents this system.